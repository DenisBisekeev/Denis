function ChangeColor(color) {
    document.body.style.background = color;
}

function red() {
    ChangeColor('red')
}
function green() {
    ChangeColor('green')
}
function yellow() {
    ChangeColor('yellow')
}
function grey() {
    ChangeColor('grey')
}
function aqua() {
    ChangeColor('aqua')
}
function white() {
    ChangeColor('white')
}